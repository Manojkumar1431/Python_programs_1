# Single quotes
print('Hello my friend')


# double quotes if there is need of single quote in the test
print(" I'm Manojkumar ")


# we can also use Double quotes inside single quotes
print(' quotation of the day "practice makes man perfect" ')


# better to use escape character symbol ( \ ) in order to escape the quotes
print(' I\'m manojkumar ')

# to print in anew line use \n
print("Hello manoj ! \n How are you")


# if we use multiple lines use triple single or double quotes
print('''hello my world 
  welcome to my world''')

print("""Hello wrold
 Welcome to my world
 Happy to see you""")